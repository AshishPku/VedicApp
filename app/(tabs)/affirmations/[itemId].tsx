import { useLocalSearchParams } from "expo-router";
import { ScrollView } from "react-native";
import React from "react";
import { AntDesign } from "@expo/vector-icons";
import img1 from "@/assets/images/background_common.webp";
import {
  View,
  Text,
  ImageBackground,
  StyleSheet,
  Pressable,
} from "react-native";
import { router } from "expo-router";

const AffirmationPractice = () => {
  const { itemId } = useLocalSearchParams();

  const findQuoteById = (id: number) => {
    for (const category of AFFIRMATION_GALLERY) {
      const quote = category.data.find((item) => item.id === id);
      if (quote) return quote;
    }
    return null;
  };

  const quoteId = Number(itemId);
  const selectedQuote = findQuoteById(quoteId);

  return (
    <View style={styles.container}>
      <ImageBackground source={img1} style={styles.background}>
        <Pressable onPress={() => router.back()} style={styles.backButton}>
          <AntDesign name="leftcircleo" size={50} color="white" />
        </Pressable>
        <View style={styles.overlay}>
          <ScrollView
            style={styles.scrollView}
            contentContainerStyle={styles.scrollViewContent}
          >
            {selectedQuote ? (
              <Text style={styles.motivationText}>{selectedQuote.text}</Text>
            ) : (
              <Text style={styles.motivationText}>Quote not found</Text>
            )}
          </ScrollView>
        </View>
      </ImageBackground>
    </View>
  );
};

export default AffirmationPractice;

